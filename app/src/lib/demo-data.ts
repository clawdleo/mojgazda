// Color for company avatar based on name hash
export function getCompanyColor(name: string): string {
  const colors = [
    "#14b8a6", "#f59e0b", "#3b82f6", "#ef4444", "#8b5cf6",
    "#ec4899", "#06b6d4", "#10b981", "#f97316", "#6366f1",
  ];
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = name.charCodeAt(i) + ((hash << 5) - hash);
  }
  return colors[Math.abs(hash) % colors.length];
}

// Get stored jobs from localStorage  
export function getStoredJobs() {
  if (typeof window === "undefined") return [];
  try {
    const raw = localStorage.getItem(JOB_STORAGE_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

export function saveJob(job: Record<string, unknown>) {
  if (typeof window === "undefined") return;
  const existing = getStoredJobs();
  existing.unshift({ ...job, id: `user-job-${Date.now()}`, created_at: new Date().toISOString() });
  localStorage.setItem(JOB_STORAGE_KEY, JSON.stringify(existing));
}