const REVIEW_STORAGE_KEY = "gazda_user_reviews";
const JOB_STORAGE_KEY = "gazda_user_jobs";

function getStoredReviews(): (Review & { company_slug: string })[] {
  if (typeof window === "undefined") return [];
  try {
    const raw = localStorage.getItem(REVIEW_STORAGE_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

function saveReview(review: Review & { company_slug: string }) {
  if (typeof window === "undefined") return;
  const existing = getStoredReviews();
  existing.unshift(review);
  localStorage.setItem(REVIEW_STORAGE_KEY, JSON.stringify(existing));
}

export function getCompanyBySlug(slug: string) {
  const company = demoCompanies.find((c) => c.slug === slug);
  if (!company) return null;
  const reviews = getCompanyReviews(slug);
  const salaries = compSalaries.filter((s) => s.company_slug === slug);
  return { company, reviews, salaries };
}

export function getAllCompanies() {
  return demoCompanies;
}

export function searchCompanies(query: string) {
  const q = query.toLowerCase();
  return demoCompanies.filter(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.industry.toLowerCase().includes(q) ||
      c.headquarters_city.toLowerCase().includes(q)
  );
}

export function getTopCompanies(limit: number = 6) {
  return [...demoCompanies].sort((a, b) => b.gazda_score - a.gazda_score).slice(0, limit);
}

export function getWorstCompanies(limit: number = 3) {
  return [...demoCompanies].sort((a, b) => a.gazda_score - b.gazda_score).slice(0, limit);
}

export function getCompanyReviews(slug: string): (Review & { company_slug: string })[] {
  const demoRevs = demoReviews.filter((r) => r.company_slug === slug);
  const storedRevs = getStoredReviews().filter((r) => r.company_slug === slug);
  return [...storedRevs, ...demoRevs];
}

export function getAllReviews(): (Review & { company_slug: string })[] {
  return [...getStoredReviews(), ...demoReviews];
}

export function getRecentReviews(limit: number = 4): (Review & { company_slug: string; company_name: string })[] {
  const all = getAllReviews();
  const sorted = all.sort((a, b) => new Date(b.created_at).getTime() - new Date(a.created_at).getTime());
  return sorted.slice(0, limit).map((r) => {
    const comp = demoCompanies.find((c) => c.slug === r.company_slug);
    return { ...r, company_name: comp?.name ?? "Nepoznata tvrtka" };
  });
}

export function addReview(review: Omit<Review, "id" | "created_at" | "helpful_count" | "is_anonymous"> & { company_slug: string }) {
  const full: Review & { company_slug: string } = {
    ...review,
    id: `user-rev-${Date.now()}`,
    created_at: new Date().toISOString(),
    helpful_count: 0,
    is_anonymous: true,
  };
  saveReview(full);
  return full;
}

